package com.sdu.uims.vo;

public class Sort {
	
	float Similarity;
	int id;
	String title;
	
	public float getSimilarity() {
		return Similarity;
	}
	public void setSimilarity(float similarity) {
		Similarity = similarity;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
}
